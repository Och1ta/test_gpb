a = [[1, 2, 3], [4, 5, 6]]
# Создаем пустой список, в который будем добавлять словари
b = []

# Проходимся по каждому подсписку в списке a
for i in a:
    # Создаем словарь для текущего подсписка
    dictionary = {
        'k1': i[0],  # Присваиваем ключу 'k1' значение первого элемента подсписка
        'k2': i[1],  # Присваиваем ключу 'k2' значение второго элемента подсписка
        'k3': i[2]   # Присваиваем ключу 'k3' значение третьего элемента подсписка
    }
    # Добавляем созданный словарь в список b
    b.append(dictionary)

# Выводим результат
print(b)

# Решение в одну строчку с использованием генератора списка
c = [{'k1': x, 'k2': y, 'k3': z} for x, y, z in a]
# Выводим результат решения в одну строчку
print("\nРешение в одну строчку:", c)
